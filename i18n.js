let currentLang = localStorage.getItem("appLang") || "en";

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("appLang", lang);
  
  if (lang === "ar") {
    document.body.setAttribute("dir", "rtl");
  } else {
    document.body.setAttribute("dir", "ltr");
  }

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang][key]) {
      el.innerText = translations[lang][key];
    }
  });

  const selectEl = document.getElementById("lang-select");
  if(selectEl) selectEl.value = lang;

  if (typeof updateWarningText === "function") {
    updateWarningText();
  }
  
  if (window.globalData && window.globalData.lastUpdated) {
    document.getElementById('lastUpdated').innerText = translations[lang].lastUpdate + " " + new Date(window.globalData.lastUpdated).toLocaleString();
  }
}

document.addEventListener("DOMContentLoaded", () => {
  setLanguage(currentLang);
});

window.translations = translations;
window.currentLang = currentLang;
window.setLanguage = setLanguage;